let currentLevel = 0;
let questionShown = [];
let subTitle = ["conection", "reflection"];

$("#card").on("click", function() {
    showRandomQuestion();
});

$(document).on("keypress", function() {
    showRandomQuestion();
});

function showRandomQuestion() {
    let currentLevelQuestions = levels[currentLevel];
    let currentQuestionsIndex;
    
    if (questionShown.length === currentLevelQuestions.length) {
        currentLevel++;
        questionShown = [];
        if (currentLevel >= levels.length) {
            alert("Fin del juego");
            return;
        }
        $("#subtitle").text("Level " + (currentLevel + 1));
    }

    do {
        currentQuestionsIndex = Math.floor(Math.random() * currentLevelQuestions.length);
    } while (questionShown.includes(currentQuestionsIndex));

    const showWilCard = Math.random() < 0.09
    if (showWilCard) {
        let wildCardIndex = Math.floor(Math.random() * wildCards.length);
        $(".question").html("<b>Wild Card:</b><br>" + wildCards[wildCardIndex]);
        $("#card").removeClass("normal");
        $("#card").addClass("wild");
    } else {
        $(".question").text(currentLevelQuestions[currentQuestionsIndex]);
        questionShown.push(currentQuestionsIndex);
        $("#card").removeClass("wild");
        $("#card").addClass("normal");
    }
}

